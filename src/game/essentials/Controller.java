package game.essentials;

public class Controller {

	public int up, down, left, right, jump, pause, special1, special2, special3;

	public Controller(int up, int down, int left, int right, int jump, int pause, int special1, int special2, int special3) {
		super();
		this.up = up;
		this.down = down;
		this.left = left;
		this.right = right;
		this.jump = jump;
		this.pause = pause;
		this.special1 = special1;
		this.special2 = special2;
		this.special3 = special3;
	}
}